package class04;

public class ForLoopDetails {

	public static void main(String[] args) {

		/*
		 * first iteration
		 * 1. initialization 2. condition 3. body of the loop 4. increment/decrement
		 * second iteration
		 * condition 3. body of the loop 4. increment/decrement
		 */
		
		int i=1;
		
		for(System.out.println("Hello");i<5;System.out.println("Bye")) {
			i++;
			System.out.println("How are you?"+i);
			System.out.println("**********");
			
		}
		
		
		
		
		
		
		
		
	}

}